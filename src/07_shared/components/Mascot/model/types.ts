/**
 * Варианты расположения "хвостика" облачка:
 * 'left' - хвостик слева (для расположения справа от маскота)
 * 'bottom' - хвостик снизу (для расположения над маскотом)
 */
type TailPosition = 'left' | 'bottom';

/**
 * Основные пропсы компонента SpeechBubble
 */
export interface ISpeechBubbleProps {
    /** Заголовок сообщения (выделяется жирным) */
    title?: string;

    /** Дополнительный контент или подпись в самом низу облачка */
    footer?: React.ReactNode;

    /** Основной текст или элементы внутри облачка */
    children: React.ReactNode;

    /** 
     * Ограничение ширины облачка в px или других единицах.
     * Позволяет тексту не растягиваться слишком сильно.
   */
    maxWidth?: string | number;

    /** Дополнительные CSS-классы для внешней стилизации */
    className?: string;
    
    /** 
     * Управляет формой облачка и положением хвостика.
     * По умолчанию обычно используется 'bottom'.
     */
    tailPosition?: TailPosition; 
}

/**
 * Пропсы для внутреннего компонента отрисовки SVG-фона
 */
export interface ISpeechBubbleBgProps {
    /** 
     * Текущая позиция хвостика, на основании которой 
     * выбирается соответствующий SVG-путь (path).
     */
    position: TailPosition;
}